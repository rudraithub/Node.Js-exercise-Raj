
//## 0504- Write a program to print day of week name using switch case.

let week = 6;

switch (week) {
    case 0:
        console.log("monday");
    case 1:
        console.log("tuesday ");
    case 2:
        console.log("wendesday");
    case 3:
        console.log("tursday");
    case 4:
        console.log("friday")
    case 5:
        console.log("saturday");
    case 6:
        console.log("sunday");

}