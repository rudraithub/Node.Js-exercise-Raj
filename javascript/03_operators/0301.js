//0301-write a program to perform all Arithmetic Operations.

let a = 56;
let b = 80;

console.log("a+b =", a + b)
console.log("a-b = ", a - b)
console.log("a*b =", a * b)
console.log("a/b = ", a / b)
console.log("a%b = ", a % b)
console.log("++a= ", ++a)
console.log("--a = ", --a)
console.log("---b =", --b)