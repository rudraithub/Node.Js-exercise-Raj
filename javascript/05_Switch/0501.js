// ## 0501- Write a program to check whether a number is positive, negative or zero using switch case.

let NUM = 23;

switch (NUM) {
    case -23:
        console.log("This is a negative ");
        break;
    case 0:
        console.log("This is a zero ");
        break;
    case 23:
        console.log(" This number is postive");
        break;
}